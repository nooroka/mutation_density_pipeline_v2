#!/bin/bash

# Цикл по всем файлам с именами, содержащими "matched" и расширением ".txt"
for file in *matched*.txt; do
  # Создаем временный файл
  temp_file=$(mktemp)

  # Обрабатываем каждый файл, заменяя последний табулятор на пробел
  sed -E 's/(.*)\t/\1 /' "$file" > "$temp_file"

  # Перемещаем временный файл на место исходного файла
  mv "$temp_file" "$file"
done
